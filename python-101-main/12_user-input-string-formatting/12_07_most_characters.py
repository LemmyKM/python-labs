# Write a script that takes three strings from the user
# and prints the longest string together with its length.
#
# Example Input:
#     hello
#     world
#     greetings
#
# Example Output:
#     9, greetings

greet1 = input('enter a word : ')
greet2 = input('enter a  second word : ')
greet3 = input('enter a third word : ')

size1 = len(greet1)
size2 = len(greet2)
size3 =  len(greet3)





print(str(size1), greet1)
print(str(size2), greet2)
print(str(size3), greet3)